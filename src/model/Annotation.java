package model;

public class Annotation {

	String text;
	String annotationType;
	
	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getType() {
		return annotationType;
	}

	public void setType(String type) {
		annotationType = type;
	}

}
